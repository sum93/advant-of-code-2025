import { expect, test } from "bun:test";
import { transformReports } from "./transformReports";

const mockInput = `7 6 4 2 1
1 2 7 8 9
9 7 6 2 1
1 3 2 4 5
8 6 4 4 1
1 3 6 7 9`;

test("should transform raw reports into arrays", () => {
  expect(transformReports(mockInput)).toStrictEqual([
    [7, 6, 4, 2, 1],
    [1, 2, 7, 8, 9],
    [9, 7, 6, 2, 1],
    [1, 3, 2, 4, 5],
    [8, 6, 4, 4, 1],
    [1, 3, 6, 7, 9],
  ]);
});
